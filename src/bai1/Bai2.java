public class Bai2 {

    public static void main(String[] args) {
        int a = 1;
        //In rồi mới tăng
        System.out.println(a++);//1
        //Tăng rồi mới in
        System.out.println(++a);//1+1=2+1=3
        //Cộng 1 và in ra rồi mới tăng
        System.out.println(a+++1);//3
        //Tăng rồi mới cộng 1 và in ra
        System.out.println(++a+1);//4+1

    }

}
